// Budget Calculator
// 1 watch = 50 tk
// 1 phone = 100 tk
// 1 laptop = 500 tk

function budgetCalculator(watch, phone, laptop){
    var watchPrice = watch * 50;
    var phonePrice = phone  * 100;
    var laptopPrice = laptop * 500;
    var totalPrice = watchPrice + phonePrice + laptopPrice;
    return totalPrice;
}
